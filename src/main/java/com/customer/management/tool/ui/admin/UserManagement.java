	private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton1ActionPerformed

		UserDetailHistory detailHistory = new UserDetailHistory();
		detailHistory.setUsername(username.getText());
		detailHistory.setEmail(email.getText());
		detailHistory.setMobile(mobile.getText());
		if (changeStatus.getSelectedItem().toString().equals(UserManagementCode.ACTIVATE.toString())) {
			detailHistory.setStatus(UserManagementCode.ACTIVATE.getPrperty());
		} else if (changeStatus.getSelectedItem().toString().equals(UserManagementCode.DEACTIVATE.toString())) {
			detailHistory.setStatus(UserManagementCode.DEACTIVATE.getPrperty());
		} else {
			detailHistory.setStatus(UserManagementCode.DELETE.getPrperty());
		}

		List<UserDetailHistory> userDetailHistory = acServiceUserImpl.getUser(detailHistory);
		if (!StringUtils.isEmpty(userDetailHistory) && !userDetailHistory.isEmpty()) {
			this.setUserDetailHistories(userDetailHistory);
			display(userDetailHistory);
		} else {
			JOptionPane.showMessageDialog(new JFrame(), "No Data Found", "Error", JOptionPane.ERROR_MESSAGE);
		}
	}// GEN-LAST:event_jButton1ActionPerformed

	public void display(List<UserDetailHistory> userDetailHistory) {

		DefaultTableModel model = new DefaultTableModel();
		model.setColumnIdentifiers(new Object[] { "Username", "Name", "Email", "Mobile", "Status" });

		// boolean[] canEdit = new boolean[] { false, false, true, true, true,
		// true };

		for (UserDetailHistory detailHistory : userDetailHistory)
			model.addRow(new Object[] { detailHistory.getUsername(), detailHistory.getName(), detailHistory.getEmail(),
					detailHistory.getMobile(), detailHistory.getStatus() });
		jTable1.setModel(model);
		// jTable1.setEnabled(false);
		jTable1.setRowSelectionAllowed(true);
		jTable1.setCellSelectionEnabled(false);
		jTable1.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

		// selectionTable.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
		model.fireTableDataChanged();
		// dataChanged(model);
	}

