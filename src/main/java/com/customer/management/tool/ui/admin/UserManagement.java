	private void updateActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_updateActionPerformed

		int selectedRowIndex = jTable1.getSelectedRow();
		if (selectedRowIndex >= 0) {
			UserDetailHistory detailHistory = new UserDetailHistory();
			detailHistory.setName(jTable1.getModel().getValueAt(selectedRowIndex, 1).toString());
			detailHistory.setEmail(jTable1.getModel().getValueAt(selectedRowIndex, 2).toString());
			detailHistory.setMobile(jTable1.getModel().getValueAt(selectedRowIndex, 3).toString());
			if (getUserDetailHistories() != null && !getUserDetailHistories().isEmpty()) {
				if (getUserDetailHistories().size() == 1) {
					detailHistory.setUsername(getUserDetailHistories().get(0).getUsername());
					detailHistory.setUserId(getUserDetailHistories().get(0).getUserId());
				} else {
					detailHistory.setUsername(getUserDetailHistories().get(selectedRowIndex).getUsername());
					detailHistory.setUserId(getUserDetailHistories().get(selectedRowIndex).getUserId());
				}
				try {
					String response = acServiceUserImpl.updateUserData(detailHistory);
					JOptionPane.showMessageDialog(new JFrame(), response, "Error", JOptionPane.INFORMATION_MESSAGE);
				} catch (Exception e) {
					JOptionPane.showMessageDialog(new JFrame(), "Something went wrong, try again", "Error",
							JOptionPane.INFORMATION_MESSAGE);
				}
			}
		} else {
			JOptionPane.showMessageDialog(new JFrame(), "Please Select atleast one Row", "Error",
					JOptionPane.ERROR_MESSAGE);
		}
	}// GEN-LAST:event_updateActionPerformed

	private void SubmitActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_SubmitActionPerformed

		try {
			int selectedRowIndex = jTable1.getSelectedRow();
			if (selectedRowIndex >= 0) {
				UserDetailHistory detailHistory = new UserDetailHistory();
				detailHistory.setUsername(jTable1.getModel().getValueAt(selectedRowIndex, 0).toString());
				detailHistory.setEmail(jTable1.getModel().getValueAt(selectedRowIndex, 3).toString());
				detailHistory.setMobile(jTable1.getModel().getValueAt(selectedRowIndex, 4).toString());
				if (changeStatus.getSelectedItem().toString().equals(UserManagementCode.ACTIVATE.toString())) {
					detailHistory.setStatus(UserManagementCode.ACTIVATE.getPrperty());
				} else if (changeStatus.getSelectedItem().toString().equals(UserManagementCode.DEACTIVATE.toString())) {
					detailHistory.setStatus(UserManagementCode.DEACTIVATE.getPrperty());
				} else {
					detailHistory.setStatus(UserManagementCode.DELETE.getPrperty());
				}

				String response = acServiceUserImpl.activateDeactivateUser(detailHistory);
				System.out.println(response);
			} else {
				JOptionPane.showMessageDialog(new JFrame(), "Please Select atleast one Row", "Error",
						JOptionPane.ERROR_MESSAGE);
			}
		} catch (Exception ex) {
			if (ex instanceof ArrayIndexOutOfBoundsException) {
				JOptionPane.showMessageDialog(new JFrame(), "Please Select atleast one Row", "Error",
						JOptionPane.ERROR_MESSAGE);
			} else {
				ex.printStackTrace();
			}
		}
	}// GEN-LAST:event_SubmitActionPerformed

